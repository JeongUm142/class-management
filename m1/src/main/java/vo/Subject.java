package vo;

public class Subject {
	private int subjectNo;
	private String subdjectName;
	private int subjectTime;
	private String createdate;
	private String updatedate;
	
	public int getSubjectNo() {
		return subjectNo;
	}
	public void setSubjectNo(int subjectNo) {
		this.subjectNo = subjectNo;
	}
	public String getSubdjectName() {
		return subdjectName;
	}
	public void setSubdjectName(String subdjectName) {
		this.subdjectName = subdjectName;
	}
	public int getSubjectTime() {
		return subjectTime;
	}
	public void setSubjectTime(int subjectTime) {
		this.subjectTime = subjectTime;
	}
	public String getCreatedate() {
		return createdate;
	}
	public void setCreatedate(String createdate) {
		this.createdate = createdate;
	}
	public String getUpdatedate() {
		return updatedate;
	}
	public void setUpdatedate(String updatedate) {
		this.updatedate = updatedate;
	}
}
